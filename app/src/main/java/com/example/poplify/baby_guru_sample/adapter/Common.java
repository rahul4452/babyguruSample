package com.example.poplify.baby_guru_sample.adapter;

public class Common {

    String card,infiniteCard;

    public Common() {
    }

    public Common(String card, String infiniteCard) {
        this.card = card;
        this.infiniteCard = infiniteCard;
    }

    public String getCard() {
        return card;
    }

    public void setCard(String card) {
        this.card = card;
    }

    public String getInfiniteCard() {
        return infiniteCard;
    }

    public void setInfiniteCard(String infiniteCard) {
        this.infiniteCard = infiniteCard;
    }
}
